void FontRenderer_Init() {}

void FontRenderer_DrawString(const char* s, float x, float y) {
    float cx = x;
    float cy = y;

    glDisable(GL_LIGHTING);
    glDisable(GL_DEPTH_TEST);

    glBegin(GL_QUADS);
    for (const char* p = s; *p; ++p) {
        unsigned char ch = static_cast<unsigned char>(*p);
        if (ch < 32 || ch > 126) ch = 32;
        int idx = ch - 32;

        for (int row = 0; row < CHAR_H; ++row) {
            unsigned char bits = FONT_DATA[idx][row];
            for (int col = 0; col < CHAR_W; ++col) {
                if (bits & (0x80 >> col)) {
                    float px = cx + col * 1.0f;
                    float py = cy + (CHAR_H - 1 - row) * 1.0f;
                    glVertex2f(px,     py);
                    glVertex2f(px + 1, py);
                    glVertex2f(px + 1, py + 1);
                    glVertex2f(px,     py + 1);
                }
            }
        }
        cx += CHAR_W;
    }
    glEnd();

    glEnable(GL_DEPTH_TEST);
    glEnable(GL_LIGHTING);
}
